SET search_path TO "utility";

DROP TABLE IF EXISTS "country_language";

CREATE TABLE "country_language" (
    "country_id" char(2) NOT NULL,
    "language_id" char(3) NOT NULL,
    "created_at" timestamptz NOT NULL DEFAULT NOW(),
    "updated_at" timestamptz NOT NULL DEFAULT NOW(),
    CONSTRAINT country_language_pkey PRIMARY KEY (country_id, language_id),
    CONSTRAINT country_language_country_fkey FOREIGN KEY (country_id) REFERENCES "country"(id) ON DELETE RESTRICT ON UPDATE RESTRICT,
    CONSTRAINT country_language_language_fkey FOREIGN KEY (language_id) REFERENCES "language"(id) ON DELETE RESTRICT ON UPDATE RESTRICT
);

CREATE TRIGGER "set_country_language_updated_at" before
UPDATE ON "country_language" FOR each ROW EXECUTE PROCEDURE set_current_timestamp_updated_at();

